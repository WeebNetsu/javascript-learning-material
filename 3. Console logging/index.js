// You can use single quotes ('), double quotes (") and ` quotes, they do the same thing
console.log("Hello World")
console.log('Hello World')
console.log(`Hello World`)

// you have multiple types of console.logs, in browser they will be different colors
// NOTE: if in browser, in dev tools in console, next to filter, change custom level to all levels to show info colors
console.log("Some text"); // display text to screen
console.error("Some error"); // display error
console.warn("Some warning"); // display warning to screen
console.info("Some Info") // display info to screen

// Escape characters
console.log("Hello\tWorld, My\nname is\vsteve! Pronounced \"Stevo\" and not 'steve\'")

/*
    \t -> tab
    \n -> new line
    \v -> verticle tab
    \" -> double quotes
    \' -> single quotes
*/